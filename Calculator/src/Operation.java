public class Operation {

    public double addition(double number_one, double number_two){
        return number_one + number_two;
    }

    public double subtraction(double number_one, double number_two){
        return number_one-number_two;
    }

    public double multiply(double number_one, double number_two){
        return number_one*number_two;
    }

    public double divide(double number_one, double number_two){
        return number_one/number_two;
    }
}
